    private void btnConfirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirActionPerformed
        String nuevoPassword = txtNewPassword.getText();
        String repetirPassword = txtRepetirPassword.getText();
        
        if (nuevoPassword.equals(repetirPassword)){
            JOptionPane.showMessageDialog(this, "Cambio exitoso de contraseña", "Cambio contraseña", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden", "Cambio contraseña", JOptionPane.WARNING_MESSAGE);
            this.dispose();
        }
        
        
    }//GEN-LAST:event_btnConfirActionPerformed

