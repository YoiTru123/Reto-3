    public void recibeDatos(int idEmp,String nombreEmp, String apellidos,String tipDocumento, String documento, String correo){
        System.out.println("IdEmp: " + idEmp + ", Nombre: " + nombreEmp + " "
                    + apellidos + ", Documento: " + tipDocumento + " " + documento
                    + ", Correo: " + correo);
        
        txtId.setText("" + idEmp);
        txtNombre.setText(nombreEmp);
        txtApellido.setText(apellidos);
        txtTdocumento.setText(tipDocumento);
        txtDocumento.setText(documento);
        txtCorreo.setText(correo);
    }
    
    public void ActualizarEmpleado(){
        int idEmp = Integer.parseInt(txtId.getText());
        String nombreEmp = txtNombre.getText();
        String apellidos = txtApellido.getText();
        String tipDocumento = txtTdocumento.getText();
        String documento = txtDocumento.getText();
        String correo = txtCorreo.getText();
        if(nombreEmp.isEmpty()){
            JOptionPane.showMessageDialog(this,"Nombre de empleado requerido.","",JOptionPane.WARNING_MESSAGE );
        }else if(apellidos.isEmpty()){
            JOptionPane.showMessageDialog(this,"Apellido de empleado requerido.","",JOptionPane.WARNING_MESSAGE );
        }else if(correo.isEmpty()){
            JOptionPane.showMessageDialog(this,"Correo de empleado requerido.","",JOptionPane.WARNING_MESSAGE );
        }else{
            String query = "UPDATE empleado SET nombreEmp='"+nombreEmp+"',apellidos='"+apellidos+"',correo='"+correo+"' WHERE idEmp = "+idEmp+";";
            System.out.println(query);
            
            try{
                connection = conexion.getCConection();
                st = connection.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this,"Usuario ha sido actualizado.","",JOptionPane.WARNING_MESSAGE );
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this,"No se actualizo el empleado.","",JOptionPane.WARNING_MESSAGE );
            }
            this.dispose();
        }
    }
    
    public void EliminarEmpleado() {
        int idEmp = Integer.parseInt(txtId.getText());
        String query = "DELETE FROM `empleado` WHERE idEmp = " + idEmp + ";";
        System.out.println(query);      
        try{
            connection = conexion.getCConection();
            st = connection.createStatement();
            st.executeUpdate(query);
            JOptionPane.showMessageDialog(this,"El usuario ha sido eliminado.","",JOptionPane.WARNING_MESSAGE );
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this,"No se elimino el empleado.","",JOptionPane.WARNING_MESSAGE );
        }
        this.dispose();
    }

