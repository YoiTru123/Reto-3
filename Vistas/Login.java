    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        //Se capturan los valores ingresados por el usuario.
        String username = txtUsername.getText();
        String password = txtPassword.getText();

        // Se imprimen los valores capturados.
        System.out.println("Username: " + username + " , password: " + password);

        // Validamos que los campos no esten vacíos.
        if (username.isEmpty() || password.isEmpty())
            JOptionPane.showMessageDialog(null, "Hay campos sin diligenciar.", "Información", JOptionPane.WARNING_MESSAGE);
        else {
            if (username.equals("MisionTIC")||username.equals("Y") && password.equals("Ciclo2")||password.equals("1")) {
                JOptionPane.showMessageDialog(null, "Bienvenido al sistema Misión TIC");

                // Se hace visible el nuevo JFrame de navegación.
                UserMenu usermenu = new UserMenu();
                usermenu.setVisible(rootPaneCheckingEnabled);
                //Eliminamos el frame de Login.
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Error al iniciar sesión.", "ERROR!", JOptionPane.WARNING_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

