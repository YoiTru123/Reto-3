    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        int tpdocumento = ccDocumento.getSelectedIndex();
        String documento = txtDocumento.getText();
        String email = txtCorreo.getText();
        ////////////////////////////////////////////////////////
        String tiDocumentoString = "";
        if(tpdocumento == 0)
            tiDocumentoString = "Cedula de ciudadania";
        else if(tpdocumento == 1)
            tiDocumentoString = "Cedula de extranjero";
        else if(tpdocumento == 2)
            tiDocumentoString = "Libreta militar";
        else if(tpdocumento == 3)
            tiDocumentoString = "Pasaporte";
        else
            tiDocumentoString = "Otro";
        ////////////////////////////////////////////////////////////
        System.out.println("Nombre: " + nombre + " " + apellido + "\nDocumento : "
                + tiDocumentoString + " " + documento + "\nCorreo: " + email);

        if(nombre.isEmpty() || apellido.isEmpty() || documento.isEmpty() || email.isEmpty() ){
            JOptionPane.showMessageDialog(this, "Faltan campos por diligenciar", "Nuevo empleado", JOptionPane.WARNING_MESSAGE);       
        }else{
            String queryCrearEmpleado = "INSERT INTO `empleado`(`nombreEmp`, `apellidos`, `tipDocumento`, `documento`, `correo`) "
                    + "VALUES ('"+nombre+"','"+apellido+"','"+tiDocumentoString+"','"+documento+"','"+email+"')";
                      
            try{
                cn = con.getCConection();
                st = cn.createStatement();
                st.executeUpdate(queryCrearEmpleado);
                JOptionPane.showMessageDialog(this, "Registro Exitoso.", "Nuevo empleado", JOptionPane.INFORMATION_MESSAGE);
                
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, "No se pudo crear el empleado.", "Nuevo empleado", JOptionPane.WARNING_MESSAGE);
            }
            this.dispose();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

